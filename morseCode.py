def toMorse(text):
    """
    Description:
    A program that converts letters to morse code.
    Doctest:
    >>> toMorse("I'm sorry, I can't let you do that....")
    '.. .----. -- / ... --- .-. .-. -.-- --..-- / .. / -.-. .- -. .----. - / .-.. . - / -.-- --- ..- / -.. --- / - .... .- - .-.-.- .-.-.- .-.-.- .-.-.-'
    """
    symbolsToMorse = {"a":".- ", "b":"-... ", "c":"-.-. ", "d":"-.. ", "e":". ", "f":"..-. ", "g":"--. ", "h":".... ", "i":".. ", "j":".--- ", "k":"-.- ", "l":".-.. ", "m":"-- ", "n":"-. ", "o":"--- ", "p":".--. ", "r":".-. ", "s":"... ", "t":"- ", "u":"..- ", "v":"...- ", "w":".-- ", "x":"-..- ", "y":"-.-- ", "z":"--.. ", " ":"/ ", "1":".---- ", "2":"..--- ", "3":"...-- ", "4":"....- ", "5":"..... ", "6":"-.... ", "7":"--... ", "8":"---.. ", "9":"----. ", ".":".-.-.- ", ",":"--..-- ", "?":"..--.. ", "-":"-....- ", "=":"-...- ", ":":"---... ", ";":"-.-.-. ", "(":"-.--. ", ")":"-.--.- ", "/":"-..-. ", '"':".-..-. ", "$":"...-..- ", "'":".----. ", "_":"..--.- ", "@":".--.-. ", "!":"---. ", "+":".-.-. ", "~":".-... ", "#":"...-.- "}
    table = str.maketrans(symbolsToMorse)
    return text.lower().translate(table).strip()

def fromMorse(symbols):
    """
    Description:
    A program that converts morse code to letters.
    Doctest:
    >>> fromMorse(".. .----. -- / ... --- .-. .-. -.-- --..-- / .. / -.-. .- -. .----. - / .-.. . - / -.-- --- ..- / -.. --- / - .... .- - .-.-.- .-.-.- .-.-.- .-.-.-")
    "i'm sorry, i can't let you do that...."
    """
    morseToSymbols = {".-":"a", "-...":"b", "-.-.":"c", "-..":"d", ".":"e", "..-.":"f", "--.":"g", "....":"h", "..":"i", ".---":"j", "-.-":"k", ".-..":"l", "--":"m", "-.":"n", "---":"o", ".--.":"p", ".-.":"r", "...":"s", "-":"t", "..-":"u", "...-":"v", ".--":"w", "-..-":"x", "-.--":"y", "--..":"z", "/":" ", ".----":"1", "..---":"2", "...--":"3", "....-":"4", ".....":"5", "-....":"6", "--...":"7", "---..":"8", "----.":"9", ".-.-.-":".", "--..--":",", "..--..":"?", "-....-":"-", "-...-":"=", "---...":":", "-.-.-.":";", "-.--.":"(", "-.--.-":")", "-..-.":"/", ".-..-.":'"', "...-..-":"$", ".----.":"'", "..--.-":"_", ".--.-.":"@", "---.":"!", "-.-.--":"!", ".-.-.":"+", ".-...":"~", "...-.-":"#"}
    result = ""
    for x in symbols.split(" "):
        try:
            result += morseToSymbols[x]
        except:
            result += x
    return result